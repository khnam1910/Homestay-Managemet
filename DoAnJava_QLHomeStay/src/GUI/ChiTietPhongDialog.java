    public void loadDVSD() {
        phucVuDAO = new PhucVuDAO();
        
        DefaultTableModel model = new DefaultTableModel(new Object[]{"Tên Dịch Vụ", "Số Lượng", "Đơn Giá"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        ArrayList<PhucVuPOJO> dspv = phucVuDAO.thongTinPhucVu(maph, makh);
        
        for (PhucVuPOJO pv : dspv) {
           
            model.addRow(new Object[]{pv.getDichVuPOJO().getTenDV(), pv.getSoluong(), pv.getDichVuPOJO().getDonGia()});
        }
        jtbDVSD.setModel(model);
    }

