    public void loadThongTin() {
        datPhongDAO = new DatPhongDAO();

        ArrayList<DatPhongPOJO> chitiet = datPhongDAO.ChiTietPhong(maph);

        for (DatPhongPOJO dp : chitiet) {
            jlbGiaPhong.setText(String.valueOf(dp.getPhongPOJO().getDongia()));
            jlbTenKH.setText(dp.getKhachHangPOJO().getTenKH());
            jlbSoNguoi.setText(String.valueOf(dp.getSoNguoi()));
            jlbNgayDP.setText(dp.getNgayDat());
            jlbNgayTP.setText(dp.getNgayTra());
        }
    }
